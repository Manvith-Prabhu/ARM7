	AREA SAMPLE,CODE,READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER
START
	LDR R0,DVD ; DIVIDEND MSB
	MOV R4,#0XD ; DIVIDEND LSB
	LDR R1,DVS ; DIVISOR
	MOV R2,#0 ; REMAINDER
	MOV R3,#1 ; 64 BIT COUNTER
; HERE DIVIDEND CHANGES TO QUOTIENT
LOOP
	CMP R3,#64
	BGT DONE
	ADD R3,R3,#1
	MOV R2,R2,LSL #1	
	MOVS R0,R0, LSL #1
	ADC R2,R2,#0
	MOVS R4,R4,LSL #1
	ADC R0,R0,#0
	SUBS R2,R2,R1
	ADDPL R4,R4,#1
	ADDMI R2,R2,R1
	B LOOP
	
DONE
	MOV R5, R0; QUOTIENT MSB
	MOV R6, R4; QUOTIENT LSB
	
STOP B STOP
DVD DCD 0XFFFFFFFF
DVS DCD 0X1FF
	END