	AREA SECOND_C, CODE, READONLY
	EXPORT RESET_HANDLER2c

RESET_HANDLER2c
START

    ;INITIALIZE THE FIRST NUMBER (LOWER 32 BITS)
    MOV R0, #10     ; SET 0(LOWER 32 BITS)
    MOV R1, #100        ; INITIALIZE THE UPPER 32 BITS OF THE FIRST NUMBER TO 100

    ; INITIALIZE THE SECOND NUMBER (LOWER 32 BITS)
    MOV R2, #10      ; SET THE ACTUAL SECOND NUMBER TO 0 (LOWER 32 BITS)
    MOV R3, #50        ; INITIALIZE THE UPPER 32 BITS OF THE SECOND NUMBER TO -50

    ; ADDITION
    ADDS R4, R0, R2    ; ADD THE LOWER 32 BITS, SET FLAGS
    ADC R5, R1, R3    ; ADD THE UPPER 32 BITS AND CARRY FLAG


    ; SUBTRACTION
    SUBS R6, R0, R2    ; SUBTRACT THE LOWER 32 BITS
    SBC R7, R1, R3    ; SUBTRACT THE UPPER 32 BITS AND CARRY FLAG

    ; REVERSE SUBTRACTION
    RSB R8, R0, R2   ; REVERSE SUBTRACT THE LOWER 32 BITS
    RSC R9, R1, R3   ; REVERSE SUBTRACT THE UPPER 32 BITS AND CARRY FLAG

STOP B STOP
	END